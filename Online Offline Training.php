      <h3 class="mb-0 mt-4">Study space management</h3>
      <p>Sometimes students might have to relocate for study purposes, but sometimes that would not be possible owing to your prior commitment. It gets as simple as travel and study away for offline mode and arrange your study space wherever you want for online mode. Choose it well as per your conveniences and needs.
      </p>

     </div>
     <div >
    
      <h3 class="mb-0 mt-4">Timescale flexibility</h3>
      <p>Time preferences go both ways, as in, convenient for both the trainer and the student, for both of them could be working professionals too. Well, some of the courses do give you videos that you could revisit and learn. Convenient batch days and timings need to be checked prior to admission. This is why the majority of the student population now chooses to learn online. This flexibility gives you the option for career advancement and even hobbies. Also in online students get to have continual support via mail or chat systems from the trainers and also individual attention.</p>
